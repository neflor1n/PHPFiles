    <h3>Переменные и история</h3>
    <p>
        Программист говорит своему коллеге:
        <br><i>— Я просто не могу понять, почему мой код не работает.</i>
        <br>Коллега спрашивает:
        <br><i>— Ты проверил, что все переменные инициализированы?</i>
        <br>Программист отвечает:
        <br><i>— Ну да, я инициализировал все переменные, но потом заметил, что моя история не была инициализирована.</i>
    </p>
