    <h3>Аптека программиста</h3>
    <p>
        Программист заходит в аптеку:
        <br><i>— У вас есть что-нибудь от усталости?</i>
        <br>Фармацевт отвечает:
        <br><i>— Есть! Только что появился новый препарат от усталости. Сделайте глубокий вдох!</i>
        <br>Программист спрашивает:
        <br><i>— А что насчёт усталости после нажатия кнопки "Сохранить"? Сколько раз нужно нажать, чтобы точно сохранилось?</i>
    </p>
